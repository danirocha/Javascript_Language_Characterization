	private void procuraArquivos(String caminho, BufferedWriter bw) throws IOException {
		
		File diretorioLido = new File(caminho);
		
		File arquivos[] = diretorioLido.listFiles();
		
		for(int i = 0; i < arquivos.length; i++) {
			File arquivo = arquivos[i];
			
			if(arquivo.isDirectory())
				procuraArquivos(arquivo.getAbsolutePath(), bw);
			
			else {
				
				boolean achou = false;
				
				for (int j = 0; j < bibliotecasErro.length; j++) {
					String biblioteca = bibliotecasErro[j];
					
					if(biblioteca.compareToIgnoreCase(arquivo.getName().replace(" R.txt", "")) == 0) {
						achou = true; break;
					}
				}
				
				if(achou)
					continue;
				
				leArquivo(arquivo, bw);
			}
		}
	}

	private void analisaArquivo(String linha, BufferedWriter bw) throws IOException {
		
		String[] dados = linha.split(";");
		
		if(dados[4].compareTo("NaN") == 0) {
			nan++;
		}
		
		nomeBiblioteca = dados[1];
		
		if(dados[2].compareTo("---") == 0) {
			
			if(nomeArquivo.compareTo(dados[0]) != 0) {	
				//File file = new File("C:\\Users\\marcio.barros\\Desktop\\dist" + nomeArquivo);
				File file = new File(dados[0]);
				tamanho = file.length();
					
				nomeArquivo = dados[0];
			}
			
			if(dados[3].compareTo("loc") == 0) {
				loc = Double.parseDouble(dados[4]);
			}
				
			else if(dados[3].compareTo("numFunc") == 0) {
				numFunc = Double.parseDouble(dados[4]);
				funcoes = Double.parseDouble(dados[4]);
			}
				
			else if(dados[3].compareTo("variaveis") == 0) {
				var = Double.parseDouble(dados[4]);
				
				if(numFunc == 0) {

					escreveDados(bw);
					loc = 0;
					var = 0;
					numFunc = 0;
					tamanho = 0;
						
					for(int h = 0; h < esprimaValores.size(); h++)
						esprimaValores.set(h, 0.0);
				}
			}
		}
		
		else {
			
			for(int j = 0; j < esprima.length; j++) {
				String noEsprima = esprima[j];
					
				if(noEsprima.compareTo(dados[3]) == 0){
					esprimaValores.set(j, esprimaValores.get(j) + Double.parseDouble(dados[4]));
					
					if(j == esprima.length - 1) {
						funcoes--;
						
						if(funcoes == 0) {
							escreveDados(bw);
							loc = 0;
							var = 0;
							numFunc = 0;
							tamanho = 0;
								
							for(int h = 0; h < esprimaValores.size(); h++)
								esprimaValores.set(h, 0.0);
						}
					}
					
					break;
				}
			}
		}
	}

	private void escreveDados(BufferedWriter bw) throws IOException {
		bw.write(nomeBiblioteca + "\t" + loc + "\t" + var + "\t" + numFunc + "\t" + tamanho + "\t");

		for(int i = 0; i < esprimaValores.size(); i++) {
			if(i < esprimaValores.size() - 1)
				bw.write(esprimaValores.get(i) + "\t");
			else
				bw.write(esprimaValores.get(i) + "\n");
		}
	}

	private void leArquivo(File arquivo, BufferedWriter bw) throws IOException {
	    BufferedReader in = new BufferedReader(new InputStreamReader(new FileInputStream(arquivo.getAbsolutePath()), "UTF8"));
	    
	    String linha;

		while ((linha = in.readLine()) != null) {
	    	
	    	if(linha.compareTo("caminho;biblioteca;nomeFuncao;analise;valor") != 0)
	    		analisaArquivo(linha, bw);
	    }
	 
		in.close();
	}

